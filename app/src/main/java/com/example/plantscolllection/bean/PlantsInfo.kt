package com.example.plantscolllection.bean

class PlantsInfo {
    val id: Int? = null

    val name: String? = null

    val userId: Int? = null

    val boId: Int? = null

    val door: String? = null

    val family: String? = null

    val latinName: String? = null

    val photo: String? = null

    val species = 0

    val time:String? = null

    override fun toString(): String {
        return "PlantsInfo(id=$id, name=$name, userId=$userId, boId=$boId, door=$door, family=$family, latinName=$latinName, photo=$photo, species=$species, time=$time)"
    }


}